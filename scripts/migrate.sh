#!/bin/bash
# =============================================================================
# Prisma 마이그레이션 스크립트
# 프로덕션 데이터베이스 마이그레이션을 안전하게 수행합니다.
# =============================================================================

set -e  # 에러 발생 시 즉시 종료

# =============================================================================
# 색상 정의
# =============================================================================
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# =============================================================================
# 헬퍼 함수
# =============================================================================
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# =============================================================================
# 환경변수 확인
# =============================================================================
check_env() {
    log_info "환경변수 확인 중..."

    if [ -z "$DATABASE_URL" ]; then
        log_error "DATABASE_URL 환경변수가 설정되지 않았습니다."
        exit 1
    fi

    if [ -z "$DIRECT_URL" ]; then
        log_warning "DIRECT_URL이 설정되지 않았습니다. DATABASE_URL을 사용합니다."
    fi

    log_success "환경변수 확인 완료"
}

# =============================================================================
# 데이터베이스 연결 테스트
# =============================================================================
test_connection() {
    log_info "데이터베이스 연결 테스트 중..."

    if npx prisma db pull --force 2>/dev/null; then
        log_success "데이터베이스 연결 성공"
    else
        log_error "데이터베이스 연결 실패"
        exit 1
    fi
}

# =============================================================================
# 스키마 검증
# =============================================================================
validate_schema() {
    log_info "Prisma 스키마 검증 중..."

    if npx prisma validate; then
        log_success "스키마 검증 통과"
    else
        log_error "스키마 검증 실패"
        exit 1
    fi
}

# =============================================================================
# 마이그레이션 상태 확인
# =============================================================================
check_migration_status() {
    log_info "마이그레이션 상태 확인 중..."
    npx prisma migrate status
}

# =============================================================================
# 마이그레이션 실행 (드라이런)
# =============================================================================
migrate_dry_run() {
    log_info "마이그레이션 드라이런 실행 중..."

    # Prisma migrate deploy는 드라이런을 지원하지 않으므로
    # 마이그레이션 상태만 확인
    check_migration_status

    log_warning "위 내용을 확인하고 실제 마이그레이션을 진행하시겠습니까?"
}

# =============================================================================
# 마이그레이션 실행
# =============================================================================
migrate() {
    log_info "마이그레이션 시작..."

    # 마이그레이션 실행
    if npx prisma migrate deploy; then
        log_success "마이그레이션 완료!"
    else
        log_error "마이그레이션 실패"
        log_warning "롤백이 필요할 수 있습니다. scripts/rollback.sh를 확인하세요."
        exit 1
    fi
}

# =============================================================================
# Prisma Client 생성
# =============================================================================
generate_client() {
    log_info "Prisma Client 생성 중..."

    if npx prisma generate; then
        log_success "Prisma Client 생성 완료"
    else
        log_error "Prisma Client 생성 실패"
        exit 1
    fi
}

# =============================================================================
# 마이그레이션 후 검증
# =============================================================================
verify_migration() {
    log_info "마이그레이션 검증 중..."

    # 스키마가 DB와 동기화되었는지 확인
    check_migration_status

    log_success "마이그레이션 검증 완료"
}

# =============================================================================
# 메인 함수
# =============================================================================
main() {
    echo "=================================================="
    echo "  바투 AI 마케팅 솔루션 - 데이터베이스 마이그레이션"
    echo "=================================================="
    echo ""

    case "${1:-deploy}" in
        "status")
            check_env
            test_connection
            check_migration_status
            ;;
        "validate")
            check_env
            validate_schema
            ;;
        "dry-run")
            check_env
            test_connection
            validate_schema
            migrate_dry_run
            ;;
        "deploy")
            check_env
            test_connection
            validate_schema
            migrate
            generate_client
            verify_migration
            ;;
        "generate")
            generate_client
            ;;
        *)
            echo "사용법: $0 {status|validate|dry-run|deploy|generate}"
            echo ""
            echo "  status    - 마이그레이션 상태 확인"
            echo "  validate  - 스키마 검증"
            echo "  dry-run   - 마이그레이션 미리보기"
            echo "  deploy    - 마이그레이션 실행 (기본값)"
            echo "  generate  - Prisma Client 생성"
            exit 1
            ;;
    esac

    echo ""
    log_success "작업이 완료되었습니다!"
}

# 스크립트 실행
main "$@"
